package aliendata;


public class Alien {

    private String codeName;
    private String bloodColor;
    private Integer noOfAntennas;
    private Integer noOfLegs;
    private String homePlanet;

    /**
     * @return the codeName
     */
    public String getCodeName() {
        return codeName;
    }

    /**
     * @param codeName the codeName to set
     */
    public void setCodeName(String codeName) {
        this.codeName = codeName;
    }

    /**
     * @return the bloodColor
     */
    public String getBloodColor() {
        return bloodColor;
    }

    /**
     * @param bloodColor the bloodColor to set
     */
    public void setBloodColor(String bloodColor) {
        this.bloodColor = bloodColor;
    }

    /**
     * @return the noOfAntennas
     */
    public Integer getNoOfAntennas() {
        return noOfAntennas;
    }

    /**
     * @param noOfAntennas the noOfAntennas to set
     */
    public void setNoOfAntennas(Integer noOfAntennas) {
        this.noOfAntennas = noOfAntennas;
    }

    /**
     * @return the noOfLegs
     */
    public Integer getNoOfLegs() {
        return noOfLegs;
    }

    /**
     * @param noOfLegs the noOfLegs to set
     */
    public void setNoOfLegs(Integer noOfLegs) {
        this.noOfLegs = noOfLegs;
    }

    /**
     * @return the homePlanet
     */
    public String getHomePlanet() {
        return homePlanet;
    }

    /**
     * @param homePlanet the homePlanet to set
     */
    public void setHomePlanet(String homePlanet) {
        this.homePlanet = homePlanet;
    }

}